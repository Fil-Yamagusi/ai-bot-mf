def set_settings(m: Message):
    user_id = m.from_user.id
    check_user(user_id)
    if m.text not in Categories and m.text not in Levels:
        logging.warning(
            f"–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å uid={user_id} –Ω–µ —Å–º–æ–≥ –∏–∑–º–µ–Ω–∏—Ç—å –Ω–∞—Å—Ç—Ä–æ–π–∫–∏")
